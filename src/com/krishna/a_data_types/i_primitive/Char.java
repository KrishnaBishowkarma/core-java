package com.krishna.a_data_types.i_primitive;

public class Char {
    public static void main(String[] args) {
        // A character data type, also known as char, is used to store a single character, such as a letter, number, or symbol
        char grade = 'A';

        System.out.println("The grade is: " + grade);

        // The Unicode value for the letter B is 65, so we could also initialize the char variable as:
        char grade2 = 66;
        System.out.println("The grade is: " + grade2);
    }
}
