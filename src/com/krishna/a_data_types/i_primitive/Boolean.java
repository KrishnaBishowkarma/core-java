package com.krishna.a_data_types.i_primitive;

public class Boolean {
    public static void main(String[] args) {
        // The boolean data type is used to store a true or false value
        boolean isStudent = true;
        boolean isTeacher = false;

        if (isStudent) {
            System.out.println("The person is a Student!");
        } else {
            System.out.println("The person is not a Student!");
        }

        if (isTeacher) {
            System.out.println("The person is a Teacher");
        } else
            System.out.println("The person is not a Teacher");
    }
}
